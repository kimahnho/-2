-- Create aac_cards table
CREATE TABLE IF NOT EXISTS public.aac_cards (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    slug text NOT NULL,
    label text NOT NULL,
    category text NOT NULL,
    emoji text NOT NULL,
    image_path text NOT NULL,
    created_at timestamp with time zone DEFAULT now(),
    UNIQUE(slug, category)
);

-- Enable RLS
ALTER TABLE public.aac_cards ENABLE ROW LEVEL SECURITY;

-- Create policy for public read access
CREATE POLICY "Enable read access for all users" ON public.aac_cards
    FOR SELECT USING (true);

-- Grant access to authenticated and anon users
GRANT SELECT ON public.aac_cards TO authenticated;
GRANT SELECT ON public.aac_cards TO anon;

